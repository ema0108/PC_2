String? generateUniqueId() {
  // Caracteres válidos para el identificador único
  const chars =
      'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';

  // Genera una cadena aleatoria de longitud 32 utilizando los caracteres válidos
  String randomString =
      List.generate(32, (index) => chars[math.Random().nextInt(chars.length)])
          .join();

  return randomString;
}
